function buildCourses(topics, direction){
  return topics.map((t, i) => {
    const [a, b] = courseColorPair(direction, i);
    return {
      id: `${direction}-${t.slug}`,
      direction,
      slug: t.slug,
      title: t.title,
      teacher: pick(TEACHERS, i + (direction === "math" ? 0 : 5)),
      lessons: t.lessons,
      duration: `${t.hours} ч`,
      durationHours: t.hours,
      level: t.level,
      rating: (4.5 + (i % 5) * 0.1).toFixed(1),
      students: 400 + i * 137 + (direction === "cs" ? 220 : 0),
      price: t.price,
      coverA: a,
      coverB: b,
      description: `Курс «${t.title}» шаг за шагом объясняет тему с нуля: от базовых определений до практических задач и мини-проектов. Много интерактивной практики и тестов после каждой темы.`,
      curriculum: buildCurriculum(t.lessons, t.title),
    };
  });
}

function buildCurriculum(lessonCount, courseTitle){
  const modules = Math.max(3, Math.round(lessonCount / 5));
  const perModule = Math.round(lessonCount / modules);
  const list = [];
  let lessonIndex = 1;
  for(let m = 1; m <= modules; m++){
    const moduleLessons = [];
    for(let l = 0; l < perModule && lessonIndex <= lessonCount; l++, lessonIndex++){
      moduleLessons.push({
        id: `l${lessonIndex}`,
        title: `${courseTitle.split(":")[0]} — урок ${lessonIndex}`,
        duration: `${8 + (lessonIndex % 6) * 2} мин`,
        done: false,
      });
    }
    list.push({ module: `Модуль ${m}`, lessons: moduleLessons });
  }
  return list;
}

